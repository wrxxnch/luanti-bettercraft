	assert (rgn1:equal_p (empty) and rgn1:is_empty ())
	local rgn1 = rgn2:op (empty, mcl_util.OP_SUB)
	assert (rgn1:equal_p (rgn2))
	local rgn1 = empty:op (rgn2, mcl_util.OP_NEQ)
	assert (rgn1:equal_p (rgn2))
end

